# Los condicionales básicamente son pedacitos de código que dicen:
  # si esto es True:
       #este código se va a ejecutar
  # si esto es False:
       #no se va a ejecutar
       
# Además, está la posibilidad de decirle:
  # si esta condición no se cumple:
    #(es decir, si el pedacito de arriba
    # es False (o falso))
       #no vamos a ejecutar el código de arriba, vamos a ejecutar el código de abajo
       
# Estos son: los if y el else
  # if: esta condición es real (True), se ejecuta el código
  # else: en caso contrario, en caso de que esta condición sea falsa (False), se ejecuta el otro código, el de abajo.
  
# Por ejemplo: 

# if edad >= 18:
#     print("Sos mayor de edad")
      
# else:
#     print("Sos menor de edad")

# Si (if) la edad es mayor o igual a 18,
# se va a decir que es mayor de edad
# sino (else), 
# se va a decir que es menor de edad
# es decir, si la condición (if edad >= 18) no se cumple, es porque no es mayor o igual a 18, o sea que tiene menos de 18 años.
# Entonces, se le dice "sos menor de edad".